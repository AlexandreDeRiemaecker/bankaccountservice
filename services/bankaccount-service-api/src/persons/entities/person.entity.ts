export class Person {
  /**
   * The unique identifier for the person (vertex ID in Neptune).
   */
  id: string;

  /**
   * The business identifier for the person.
   */
  personId: string;

  /**
   * The name of the person.
   */
  name: string;

  /**
   * The email address of the person.
   */
  email: string;
}
